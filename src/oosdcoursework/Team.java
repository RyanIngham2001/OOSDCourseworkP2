package oosdcoursework;

public class Team {
    String name;
    int matchesPlayed;
    int matchesWon;
    int setsWon;

    public Team (String name, int matchesPlayed, int matchesWon, int setsWon) {
        this.name = name;
        this.matchesPlayed = matchesPlayed;
        this.matchesWon = matchesWon;
        this.setsWon = setsWon;
    }
}
